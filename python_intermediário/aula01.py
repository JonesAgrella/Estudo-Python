'''
Introdução às funções (def) em Pynthon 
Funções são trechos de código usado para replicar
deterninada ação ao longo do seu código.
Eças podem receber valores para parâmetros (argumentos)
e retornar um valor específico.
Por padrão, funções Pynthon retornam None.
'''

def imprimir(a, b, c):
    print(a, b, c)

imprimir(1, 2, 3)
